package Day10.java;

public class Main {
    
    public static void main(String[] args) {
         final double tinggi = 165.0;
        System.out.println(tinggi);
         
        /* constanta (final variabel) itu nilai yang sudah
         kita masukkan tidak bisa kita ubah atau update nilai variabelnya
        contohnya jika kita menambah kan nilai lain maka itu bakal eror
        karena final variabel itu tidak bisa mengupdate nilai variabel*/
        
    }
    
}
